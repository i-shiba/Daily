/*ほかの変数の値を代入する*/

package M08D31;

public class M08D31_03 {
	public static void main(String[] args){
		Integer num1 , num2;
		num1 = 3;
		System.out.println("変数num1の値は" + num1 + "です。");
		num2 = num1;
		System.out.println("変数num1の値を変数num2に代入しました。");
		System.out.println("変数num2の値は" + num2 + "です。");
	}

}
