/*いろいろな演算子を利用する*/

package M09D04;

public class M09D04_04{
	public static void main(String[] args){
		Integer num1 = 10;
		Integer num2 = 5;
		
		System.out.println("いろいろな演算を行います。");
		System.out.println("num1+num2は" + (num1 + num2) + "です。");
		System.out.println("num1-num2は" + (num1 - num2) + "です。");
		System.out.println("num1*num2は" + (num1 * num2) + "です。");
		System.out.println("num1/num2は" + (num1 / num2) + "です。");
		System.out.println("num1%num2は" + (num1 % num2) + "です。");
	}
}